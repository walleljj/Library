	private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton3ActionPerformed
		try {
			// TODO add your handling code here:
			String id = jTextField2.getText();
			String name = jTextFieldName.getText();
			String pass = jPasswordFieldPass.getText();
			int age = Integer.parseInt(jTextFieldAge.getText());
			String sex = null;
			if (jRadioButton1.isSelected())
				sex = jRadioButton1.getText();
			if (jRadioButton2.isSelected())
				sex = jRadioButton2.getText();
			if (new MangerReader().isHave(id)) {
				Reader reader = new Reader(id, pass, name, age, sex);
				new MangerReader().changeReader(reader);
				JOptionPane.showMessageDialog(null, "修改成功");
				setVisible(false);
			} else {
				JOptionPane.showMessageDialog(null, "无此人");
			}
		} catch (Exception ex) {
			Logger.getLogger(GuanliReader.class.getName()).log(Level.SEVERE, null, ex);
		}

	}// GEN-LAST:event_jButton3ActionPerformed

