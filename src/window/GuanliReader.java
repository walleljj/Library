	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
		try {
			// TODO add your handling code here:
			int lorr = new LoginJFrame().getLorr();
			if (lorr == 0) {
				String id = jTextField1.getText();
				if (new MangerReader().isHave(id)) {
					new MangerReader().deletReader(id);
					JOptionPane.showMessageDialog(null, "删除成功");
					setVisible(false);
				} else {
					JOptionPane.showMessageDialog(null, "无此人");
				}
			} else
				JOptionPane.showMessageDialog(null, "非管理员，没有权限操作");
		} catch (Exception ex) {
			Logger.getLogger(GuanliReader.class.getName()).log(Level.SEVERE, null, ex);
		}

	}// GEN-LAST:event_jButton1ActionPerformed

